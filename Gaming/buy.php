<h5>Description</h5>
<p>Call of Duty: Modern Warfare II is a 2022 first-person shooter video game
developed by Infinity Ward and published by Activision. It is a sequel to the 2019 reboot,
and serves as the nineteenth installment in the overall Call of Duty series.[2] It was
released on October 28, 2022, for PlayStation 4, PlayStation 5, Windows, Xbox One, and
Xbox Series X/S.[3]

Like its predecessor, the game takes place in a realistic and modern setting. The
campaign follows multi-national special operations unit Task Force 141 and Mexican
Special Forces unit Los Vaqueros as they attempt to track down terrorist Hassan Zyani,
who is in possession of American-made ballistic missiles. Powered by a new version of the
IW engine, Modern Warfare II continues to support cross-platform multiplayer and also
features a free-to-play battle royale mode, Warzone 2.0, a follow-up to the original
Warzone, all within a single cross-game launcher, known as Call of Duty HQ.

Modern Warfare II received generally favorable reviews from critics. It was a
commercial success and broke several records for the series, including becoming the
fastest Call of Duty game to generate US$1 billion in revenue.

A sequel, titled Modern Warfare III, is scheduled to be released into Call of Duty HQ,
on November 10, 2023.</p>
<p>Modern Warfare II introduces several design improvements and changes to the
series gameplay, such as advanced AI systems in the campaign and co-op modes, water
physics, swimming mechanics, and an overhauled vehicle system. New gameplay features
and movement tactics include dive to prone, mantle, and ledge hang along with the
removal of slide canceling. New vehicle gameplay features include leaning out of vehicle

windows, mantling onto a vehicle roof, and hijacking.[4][5][6][7][8] The levelling and
Gunsmith system has been revamped, allowing players to fine-tune specific attachments
to suit their playstyles. It also offers weapon platforms that branch progression to reduce
repetitiveness, as well as featuring a firing range for practice.[9][10][11]

Modern Warfare II multiplayer features several new game modes: Knockout, in
which two teams attempt to capture a package with limited lives; and Prisoner Rescue, in
which an attacking team attempts to extract a hostage while a defending team prevents
them by fortifying defenses around the hostage.[12] Third-person game modes were
confirmed in September 2022.[13] The cooperative Special Ops mode also returns,
featuring two-player missions, with an additional 3-player activity called Raids released
post-launch.[14] Additional multiplayer modes were also released as part of post-launch
seasons, including the fan-favorite Gunfight mode from Modern Warfare (2019) and Call of
Duty: Black Ops Cold War.[15]

Like the previous game, Modern Warfare II features a free-to-play battle royale
game mode called Warzone 2.0, which is introduced with the first seasonal content update
for the game on November 16.[16] In addition to the traditional battle royale mode,
Warzone 2.0 also introduces DMZ, a new extraction game mode in which teams of trios
battle against each other as well as AI combatants, while attempting to complete missions
and extract loot from the playable map.</p>
